<h4 class="mb-0">Isomara MYO Design Guide</h4>
    <p class="mb-2">
        Inherently symmetrical, split an Isomara down the middle and the markings, patterns, and colors will be the same on each side (we are lenient on this guideline if the markinga are more natural or complex like a house cat with a mackerel coat or a cheetah with various-sized and many spots).
        An Isomara can be unsymmetrical if they have the Unsymmetrical Special Trait.
    </p>

