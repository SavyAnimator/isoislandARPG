<h4>Redesigning Your Characters</h4>

<h5 style="text-align: left;">Useful Terms</h5>
    <ul>
        <li style="text-align: left;"><strong>MYOs</strong> are characters that were designed and created by players who are not <a href="https://isomara-island.com/info/credits">staff or guest artists</a>.</li>
        <li style="text-align: left;"><strong>Customs</strong> are characters that were commissioned by staff or official guest artists to design and create. Sometimes, staff will open commissions where players can pay and provide details or a theme and traits they want and the staff will come up with a design.</li>
        <li style="text-align: left;">All other characters are <strong>adoptables</strong> which were made by staff or official guest artists and sold to whoever had an interest in the design.</li>
    </ul>

<h5>Redesigning Your MYO</h5>
    <p>
        Everyone is allowed one (1) free makeover of each MYO, so if you happen to not like the MYO after you've designed and gotten it approved, you may give it a makeover (You may have someone else create/remake your MYO).
    </p>
    <p>
        If you happen to own a MYO character you might not find the design/traits exactly to your liking. Every person is allowed to redesign their MYO.
        If you are unsure if you own a MYO character you can find the information in the Change Log history on the character's master list page. If the Change Log is unclear on whether it was a MYO you can submit a Help/Question Report in the <a href="https://isomara-island.com/reports/bug-reports">Support Center</a> or in the official Discord.
    </p>
    <p>
        Every MYO may be redesigned once per owner for free.
        <ul>
            <li><b>Example 1:</b> If SlayersStronghold happens to make a MYO and then does not like it they may redesign it thus using their one free redesign.</li>
            <li><b>Example 2:</b> SirPacings is given the MYO Isomara that SlayersStronghold owned. Even though SlayersStronghold redesigned it SirPacings still gets one free redesign of the MYO.             If the MYO was then given back to SlayersStronghold after they would not be able to redesign it as they've already used their free redesign on that specific MYO.
            </li>
        </ul>
        If you happen to use your free redesign of your MYO you may purchase the ability to get another redesign of it.
    </p>
    <br>
<h5>Redesigning Your Custom</h5>
    <p>
        Characters created from Custom slots are now allowed one (1) free redesign. We understand that sometimes a player can fall out of love with a design or may not have liked certain aspects of the custom they received.
        Redesigning a custom character can only be done once and only by the original owner of the custom character (The person who the custom was made for). If the custom character has a new owner that new owner does not get a redesign as it was not their custom.
    </p>
    <br>
