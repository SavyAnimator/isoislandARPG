<h5>Colors & Markings</h5>
    <p class="mb-2">
        Isomara are a colorful and vibrant species of creature. You may use any amount of colors and kinds of markings when designing a MYO.
        All colors of the rainbow can be used as well. The only limitation is Isomara cannot have overly dark and desaturated colors without the Special Black Primay Trait.
    </p>
<h5>Cheek Fluff</h5>
    <p class="mb-2">
        Cheek fluff is a stylistic feature on an Isomara and completely optional, but makes isomara more identifiable. It is typical for isomara to sport three fluffy tufts on thier cheeks, but you may change the amount, whether they are more round and puffy, sharp and jagged, heavy and wilted, or other design choice.
        If the Isomara has cheek spiked horns cheek fluff can be added inbetweeb each spike or be omitted allthogether.
    </p>
<h5>Hair</h5>
    <p class="mb-2">
    Hair including head hair, mustaches, and goatees are allowed to be added and changed whenever you feel like and can be any length and styled with simple clips and hair ties without the need for accessories.
    </p>
    <img width="100%" src="/files/DesignGuide/Hair.png"/>
<br><br>
<h5>Scars & Disabilities</h5>
    <p class="mb-2">
        You are completely allowed to add and scars, and remove parts of limbs at will without the need for any items as it is a customization and character development aspect.
        Impairments on an Isomara's body in the ARPG are something you may fully edit whenever you'd like, but you will need to make custom accessories if you'd like to have prosthetics.
    </p>

