<h4 class="mb-0">Seashells - In-Game Currency</h4>
    <p class="mb-2">Seashells (also known as 'ss') are used for nearly all transactions and are used to purchase items, traits, and participate in some events and activities. Seashells can be earned through submitting artwork and written media about yours or someone else's Isomara Island characters.
    You can also trade items to other members and sell items from your inventory in return for seashells. Read up on all the ways to <a href="/info/currency">earn in-game Currency here</a></p>

    <p class="mb-2"><b>TIP:</b> <a href="/NPCs">NPCs [WIP]</a> are always available to be added into media for ss, no need to ask! Also, the <a href="">Isomara and companion species of the month featured [WIP]</a> grant bonus ss (The Isomara and comapanion species of the Month can always be found on the <a href="/">homepage</a>).
    </p>
