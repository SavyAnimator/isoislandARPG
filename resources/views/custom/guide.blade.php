<h4 class="mb-0">The Main Island</h4>
    <p class="mb-2">Isomara make their home on a large Island away from any other bodies of land. The area of the island is roughly 490,000 squared miles (about 1,269,000 squared kilometers).<br>
    The island has no formal name, but smaller areas of land around the island sometimes do have names given to them. One smaller island of which is called Pelemoku and another Fross. One popular and important activity within the ARPG is <a href="/explore">exploration</a> of the island and there is also <a href="https://www.deviantart.com/isomara-island/journal/Activity-Voyaging-723078861">voyaging</a> an activity that challenges Isomara to roam outside the confines of their home island and into the vast unexplored world.</p>

