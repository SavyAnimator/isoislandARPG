<h4 class="mb-0">What are Isomara?</h4>
    <p class="mb-2">Isomara (Eye-so-mare-ah), or for short Isos, are semi-aquatic mammals with huge paws and manes and are often classified as dragons. They have feathered wings and large rounded canine teeth. They are sentient creatures with the ability to use tools, farm, herd, and even tame local fauna. They are tribal creatures who live rather simple lives. Isomara typically live in colonies which are not run by any individual but instead is formed and ran through the word of the wise. Typically older Isomara have a larger influence than younger, however, younger Isomara often get a large say in matters as they can bring forth new ideas into the society.
    </p>
    <p class="mb-2">Isomara are the primary playable characters of the game. Aside from Isomara, there are other species on the island that members can own such as Gooms and Memora. These other species are referred to as companions and help give buffs and benefits to Isomara. Not all activities in this ARPG require an Isomara there are some games and activities which only companions can partake in.
    </p>
        <br>

