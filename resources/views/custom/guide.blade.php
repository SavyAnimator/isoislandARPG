<h4 class="mb-0">How do I get started in the ARPG and get an Isomara?</h4>
    <p class="mb-2">Before seeking out a character understand that by signing up for an account and owning a character on this site that you are agreeing to the <a href="/info/terms">Terms of Service</a> and <a href="/info/privacy">Privacy Policy</a>.</p>

<h4 class="mb-0">How to obtain an Isomara and other island inhabitants:</h4>
    <p class="mb-2"><b><u>Adoptables</u></b> - The <a href="/sales">sales page</a> will have news of open adoptables soon as their live. All adopts are posted to the <a href="https://www.patreon.com/IsomaraARPG">Isomara Island Patreon</a> first (up to 3 days before public release).
    After at least 24hrs on Patreon then adoptable designs are uploaded to <a href="https://www.deviantart.com/isomara-island/gallery/69250754/open-guest-artist-adopts">DeviantArt</a> and <a href="/sales">onsite here</a>. Pledging to the Patreon is the quickest way to spot adoptables early,
     otherwise watching <a href="https://www.deviantart.com/slayersstronghold">SlayersStronghold on DeviantArt</a> or any of the other <a href="/info/team">Guest Artists' Socials</a> is the next best option. Consider pledging to the Patreon for the ability to claim sales early, see sneak peaks, earn discounts, and earn other in-game rewards.
    </p>
    <p class="mb-2"><b><u>The Daycare</u></b> - An orphanage run by NPCs, Carmen and Frio, at <a href="/adoptions">The Daycare</a> you can adopt characters including Isomara, eggs, and MYO slots for in-game currency. There are also times where there will be free eggs and Isolings (child age Isomara).</p>

    <p class="mb-2"><b><u>User Tades & Sales</u></b> - The <a href="/masterlist">masterlist</a> advanced search has a Trading Status toggle to look through characters which are up for trade/sale by other players.
    You can also find other players selling and trading characters in the Advertisements channel of the Isomara Island <a href="https://discord.gg/62ZGCYFUDu">Discord server</a>.
    </p>

    <p class="mb-2"><b><u>MYO Slots</u></b> - Are Make-Your-Own character slots that can be purchased using <a href="/info/currency">in-game currency</a> or in-real-life money. These slots are blank slates that allow you to design a character yourself. The <a href="/info/myo">MYO Slot Information</a> guide has a comprensive list of pricing along with shop links and plenty of info on how to make and design characters.
    </p>
    <p class="mb-2">Additionally, there are some random events where you may have the ability to win an Isomara or other species character. These events can take the form of Draw-to-Adopts, raffles, and contests. Keep an eye on our <a href="/news">news</a> for events like these.</p>
